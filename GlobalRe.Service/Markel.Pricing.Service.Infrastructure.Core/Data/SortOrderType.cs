namespace Markel.Pricing.Service.Infrastructure.Data
{
    /// <summary>
    /// Enum to specify Sort Order
    /// </summary>
    public enum SortOrderType
    {
        /// <summary>
        /// Sets sort order to None
        /// </summary>
        None = 0,
        /// <summary>
        ///  Sets sort order to sort Ascending
        /// </summary>
        Ascending = 1,
        /// <summary>
        /// Sets sort order to sort Descending
        /// </summary>
        Descending = 2
    }
}
